import turtle
# set turtle colour to black
turtle.color((0,0,0))
# draw higher base of ship
turtle.begin_fill()
turtle.forward(200)
turtle.right(130)
# save location half-way to colour bottom half red later
turtle.forward(40)
red_pos = turtle.pos()
red_heading = turtle.heading()
# finish drawing base
turtle.right(50)
turtle.forward(150)
turtle.right(50)
turtle.forward(40)
turtle.seth(0)
turtle.forward(20)
turtle.end_fill()
# fill in lower part with red
turtle.color("red")
turtle.penup()
turtle.setpos(red_pos)
turtle.seth(red_heading)
turtle.pendown()
turtle.begin_fill()
turtle.forward(40)
turtle.right(50)
turtle.forward(100)
turtle.right(50)
turtle.forward(40)
turtle.right(130)
turtle.forward(20)
turtle.end_fill()
# time for chimneys
def chimney():
    turtle.pendown()
    turtle.color("orange")
    turtle.begin_fill()
    turtle.left(110)
    turtle.forward(35)
    turtle.right(90)
    turtle.forward(20)
    turtle.right(90)
    turtle.forward(50)
    turtle.end_fill()
    turtle.penup()
turtle.penup()
turtle.home()
turtle.forward(60)
chimney()
turtle.home()
turtle.forward(130)
chimney()